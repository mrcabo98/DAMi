# Ejercici 2 - L'usuari escriu dos enters i s'imprimeix la suma, la resta, multiplicació, divisió i potència dels dos.

num1 = int(input("Escribe el primer número : "))
num2 = int(input("Escribe el segundo número : "))

print("*************************")
print("SUMA = " + str(num1 + num2))
print("RESTA = " + str(num1 - num2))
print("MULTIPLICACION = " + str(num1 * num2))
print("DIVISION = " + str(num1 / num2))
print("POTENCIA NUM1 = " + str(num1 ^ num1))
print("POTENCIA NUM2 = " + str(num2 ^ num2))
print("*************************")
